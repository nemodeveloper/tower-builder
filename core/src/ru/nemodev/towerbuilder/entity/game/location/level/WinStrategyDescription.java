package ru.nemodev.towerbuilder.entity.game.location.level;

public class WinStrategyDescription
{
    private int count;
    private String type;

    public WinStrategyDescription()
    { }

    public int getCount()
    {
        return count;
    }

    public void setCount(int count)
    {
        this.count = count;
    }

    public String getType()
    {
        return type;
    }

    public void setType(String type)
    {
        this.type = type;
    }
}
